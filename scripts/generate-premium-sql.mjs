/**
 * Reads supabase/premium_urls.txt and generates SQL to upsert all
 * premium / hack Instagram recipes into the database.
 *
 * Usage:
 *   node scripts/generate-premium-sql.mjs
 *
 * Then paste the printed SQL into the Supabase SQL Editor.
 */

import { readFileSync, writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dir = dirname(fileURLToPath(import.meta.url));
const txtPath = resolve(__dir, "../supabase/premium_urls.txt");
const outPath = resolve(__dir, "../supabase/generated_premium.sql");

const lines = readFileSync(txtPath, "utf8").split(/\r?\n/);

let section = null;
const hacks = [];
const premium = [];

for (const raw of lines) {
  const line = raw.trim();
  if (!line || line.startsWith("#")) continue;
  if (line === "[hacks]")   { section = "hacks";   continue; }
  if (line === "[premium]") { section = "premium"; continue; }
  if (!line.startsWith("http")) continue;

  const url = line.replace(/\/$/, "") + "/"; // normalise trailing slash
  if (section === "hacks")   hacks.push(url);
  if (section === "premium") premium.push(url);
}

function escape(s) {
  return s.replace(/'/g, "''");
}

const lines_out = [];

lines_out.push("-- Auto-generated by scripts/generate-premium-sql.mjs");
lines_out.push("-- Run this in the Supabase SQL Editor to sync premium_urls.txt\n");

// ── HACKS ──────────────────────────────────────────────────────────────────
if (hacks.length) {
  lines_out.push("-- ── HACKS ─────────────────────────────────────────────────────");
  lines_out.push("insert into recipes (");
  lines_out.push("  source, source_name, source_url,");
  lines_out.push("  title, description, image_url,");
  lines_out.push("  cuisine_type, dish_types, dietary_tags,");
  lines_out.push("  ingredients, instructions,");
  lines_out.push("  is_premium, is_hack,");
  lines_out.push("  prep_time_minutes, cook_time_minutes, servings");
  lines_out.push(") values");

  hacks.forEach((url, i) => {
    const comma = i < hacks.length - 1 ? "," : "";
    lines_out.push(
      `('social','Instagram','${escape(url)}',` +
      `'Kitchen Hack #${i + 1}',` +
      `'A viral kitchen trick — watch the original reel for the full technique.',` +
      `'https://images.unsplash.com/photo-1556909114-f6e7ad7d3136?w=800&q=80',` +
      `'General',ARRAY['hack'],ARRAY[]::text[],` +
      `'[]'::jsonb,ARRAY['See Instagram post for full technique.'],` +
      `true,true,0,0,1)${comma}`
    );
  });

  lines_out.push("on conflict (source_url) do nothing;\n");
}

// ── PREMIUM ─────────────────────────────────────────────────────────────────
if (premium.length) {
  lines_out.push("-- ── PREMIUM RECIPES ────────────────────────────────────────────");
  lines_out.push("insert into recipes (");
  lines_out.push("  source, source_name, source_url,");
  lines_out.push("  title, description, image_url,");
  lines_out.push("  cuisine_type, dish_types, dietary_tags,");
  lines_out.push("  ingredients, instructions,");
  lines_out.push("  is_premium, is_hack,");
  lines_out.push("  prep_time_minutes, cook_time_minutes, servings");
  lines_out.push(")");
  lines_out.push("select");
  lines_out.push("  'social','Instagram', url,");
  lines_out.push("  'Premium Recipe',");
  lines_out.push("  'Exclusive recipe from our curated Instagram collection. Click to view the full post.',");
  lines_out.push("  'https://images.unsplash.com/photo-1490645935967-10de6ba17061?w=800&q=80',");
  lines_out.push("  'Social',ARRAY['premium']::text[],ARRAY[]::text[],");
  lines_out.push("  '[]'::jsonb,ARRAY['See the Instagram post for the full recipe.'],");
  lines_out.push("  true,false,0,0,1");
  lines_out.push("from (values");

  premium.forEach((url, i) => {
    const comma = i < premium.length - 1 ? "," : "";
    lines_out.push(`  ('${escape(url)}')${comma}`);
  });

  lines_out.push(") as t(url)");
  lines_out.push("on conflict (source_url) do nothing;");
}

const sql = lines_out.join("\n");
writeFileSync(outPath, sql, "utf8");

console.log(`✓ Written to supabase/generated_premium.sql`);
console.log(`  ${hacks.length} hacks, ${premium.length} premium recipes`);
console.log(`\nPaste that file into the Supabase SQL Editor to sync.`);
